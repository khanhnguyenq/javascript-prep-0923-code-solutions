function addTwoNumbers(num1, num2) {
  return num1 + num2;
}
console.log('addTwoNumbers exercise: ', addTwoNumbers(9, 10));

function convertHoursToMinutes(hours) {
  return hours * 60;
}
console.log('converHoursToMinutes Exercise: ', convertHoursToMinutes(9));

function getGreeting(name) {
  return `Hello ${name}!`;
}
console.log('getGreeting Exercise: ', getGreeting('Khanh'));

function addAndMultiplyBy5(num1, num2) {
  return (num1 + num2) * 5;
}
console.log('addAndMultipleBy5 Exercise: ', addAndMultiplyBy5(2, 3));

function multiplyAndDivideBy5(num1, num2) {
  return (num1 * num2) / 5;
}
console.log('multiplyAndDivideBy5 Exercise: ', multiplyAndDivideBy5(35, 10));

function subtractTwoNumbers(num1, num2) {
  return num1 - num2;
}
console.log('subtractTwoNumbers Exercise: ', subtractTwoNumbers(91, 10));

function getCircleCircumference(radius) {
  return 2 * Math.PI * radius;
}
console.log('getCircleCircumference Exercise: ', getCircleCircumference(5));

function getFullName(firstName, lastName) {
  return `${firstName} ${lastName}`;
}
console.log('getFullName Exercise: ', getFullName('Khanh', 'Nguyen'));

function cube(number) {
  return number * number * number;
}
console.log('cube Exercise: ', cube(4));
